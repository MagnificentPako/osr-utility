package me.h3x0rz.osr.lifegraph;

public class LifeGraphElement {

    private int u;
    private double v;

    public LifeGraphElement(int u, double v) {
        this.u = u;
        this.v = v;
    }

    public int getU() {
        return u;
    }

    public double getV() {
        return v;
    }
}
